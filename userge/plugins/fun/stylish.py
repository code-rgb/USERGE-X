def font_gen(type_choice: str, input_str: str):
    font_type = list(Styled.all_fonts[type_choice])
    for _ in input_str:
        if _ in Styled.normal:
            new_char = font_type[Styled.normal.index(_)]
            input_str = input_str.replace(_, new_char)
    return input_str


@userge.on_cmd(
    "style",
    about={
        "header": "Make Font stylish",
        "description": "create text in style",
        "usage": "{tr}style -sans [text | reply]",
        "themes": Styled.font_flags,
    },
    del_pre=True,
)
async def _style_text(message: Message):
    """Make text stylish"""
    reply = message.reply_to_message
    args = message.filtered_input_str or reply.text
    if not args:
        await message.err("See Help !", del_in=5)
        return
    await message.edit("ğŸ§™â€â™‚ï¸ `Doing some magik ...`")
    if message.flags:
        font_choice = list(message.flags.keys())[0]
        input_str = message.filtered_input_str or reply.text
        if font_choice not in Styled.font_choice:
            await message.err("Flag is Invalid", del_in=5)
            return
    else:
        font_choice = rand_array(Styled.font_choice)
        input_str = args
    styled_msg = font_gen(font_choice, input_str.strip())
    await message.edit(styled_msg)
